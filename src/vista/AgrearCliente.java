    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        PCliente pv = PCliente.getInstacia();
        String nombre = tnombre.getText();
        String ident = tident.getText();
        if (nombre.equals("")) {
            JOptionPane.showMessageDialog(this, "El campo 'Nombre' no puede estar vacio.", "Advertencia!", JOptionPane.WARNING_MESSAGE);
        } else if (ident.equals("")) {
            JOptionPane.showMessageDialog(this, "El campo 'Ident.' no puede estar vacio.", "Advertencia!", JOptionPane.WARNING_MESSAGE);
        } else {
            pv.addCliete(new Cliente(nombre, ident));
            doClose(RET_OK);
        }
    }//GEN-LAST:event_okButtonActionPerformed

